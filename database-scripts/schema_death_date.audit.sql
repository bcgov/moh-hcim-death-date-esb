SPOOL schema_death_date_audit.log;

DROP USER ESB_DEATH CASCADE;

-- NOTE: confirm password
CREATE USER ESB_DEATH
  IDENTIFIED BY ***
  DEFAULT TABLESPACE ESB_DEATH_REG_DATA
  TEMPORARY TABLESPACE TEMP
  PROFILE DEFAULT
  ACCOUNT UNLOCK;

  -- 2 Roles for ESB_DEATH 
  GRANT CONNECT TO ESB_DEATH;
